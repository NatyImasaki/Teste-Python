valores = [1, 7, 7, 19, 3, 2, 11, 15, 6, 1, 5]
print(valores)

#quantas vezes aparece um determinado elemento
contagem = valores.count(7)
print(contagem)

#ordenando a lista
valores.sort()
print(valores)

#invertendo a lista
valores.reverse()
print(valores)

#tamanho da lista
tamanho = len(valores)
print(tamanho)

#soma dos elementos
soma = sum(valores)
print(soma)